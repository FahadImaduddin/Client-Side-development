//  Comparison Operator
var a = 3;
console.log(a == 6); //return false
console.log(a === 6); //return false
console.log(a != 6); //return true
console.log(a !== 6); //return true
console.log(a > 6); //return false
console.log(a < 6); //return true
console.log(a >= 6); //return false
console.log(a <= 6); //return true

